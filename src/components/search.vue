<template>
  <Input
    icon="ios-search"
    :placeholder="$t('search.placeholder')"
    style="width: 40%;text-align: left;"
    @on-enter="handlerSearch" :value="words">
  </Input>
</template>
<script>

  export default {
    data() {
      return {
        words: '',
      }
    },
    created() {
      if (this.$route.name === 'search' && this.words === '') {
        this.words = this.$route.params.words
      }
    },
    methods: {
      handlerSearch(e) {
        this.$router.push({
          name: 'search',
          params: {
            'lang': this.$i18n.locale,
            'words': e.target._value
          }
        })
      }
    }
  }
</script>
